# integer
a = 10
print('a = ', a, 'bertipe = ', type(a))

# float
a = 10.5
print('a = ', a, 'bertipe = ', type(a))

# string
a = 'sepuluh'
print('a = ', a, 'bertipe = ', type(a))

# boolean
a = True
print('a = ', a, 'bertipe = ', type(a))


# tipe data khusus
# bilangan kompleks
a = complex(10.6)
print('a = ', a, 'bertipe = ', type(a))

# tipe data dari bahasa C
from ctypes import c_double
a = c_double(5.5)
print('a = ', a, 'bertipe = ', type(a))






